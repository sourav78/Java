public class Datatypes {
    public static void main(String[] args) {

        //Premitive Types

        //byte - 1
        //short - 2
        //int - 4
        //long - 8
        //float - 4
        //double - 8
        //char - 2
        //boolean - 1

        byte age = 33;
        int number = 785412528;
        long longNumber = 7854125263985l;
        short weight = 333;
        float salary = 4521.90f;
        double score = 784.2362012d;
        char letter = 's';
        boolean isActive = false;


        //Non-Premitive Types

        //String
        String name = "Sourav";
        String friend = new String("My friend");
    }
}
